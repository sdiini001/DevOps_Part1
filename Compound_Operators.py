number1 = 10
number2 = 5
print(number1, number2)
# long hand syntax
# number1 = number1 + number2
number1 += number2
print(number1, number2)


# number1 = number1 - number2
number1 -= number2
print(number1, number2)


# do the same with multiply and divide
# include the long-hand and the short-hand syntax
# number1 = number1 / number2
number1 /= number2
print(number1, number2)

# number1 = number1 * number2
number1 *= number2
print(number1, number2)

number1 = 20
number2 = 3
print(number1, number2)
# % is modulus gives you the remainder
# do a divide and what's remaining
# number1 = number1 % number2
number1 %= number2
print(number1, number2)